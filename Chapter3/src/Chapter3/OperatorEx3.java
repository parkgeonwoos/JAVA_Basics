package Chapter3;

public class OperatorEx3 {

	public static void main(String[] args) {
		int num1 = -10; //변수 선언
		int num2 = -20; //변수 선언 
		System.out.println(+num1); //부호 + 출력
		System.out.println(+num2); //부호 + 출력
		System.out.println(-num1); //부호 - 출력
		System.out.println(-num2); //부호 - 출력
		System.out.println(num1);//부호를 직접적으로 바꾸어주지 않아서 그대로 저장된 값이 나옴
		System.out.println(num2);//부호를 직접적으로 바꾸어주지 않아서 그대로 저장된 값이 나옴

	}

}
